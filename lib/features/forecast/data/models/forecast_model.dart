class ForecastModel extends Equatable {
  final Location location;
  final Forecast forecast;

  const ForecastModel({
    required this.location,
    required this.forecast,
  });

  factory ForecastModel.fromJson(Map<String, dynamic> json) => ForecastModel(
        location: Location.fromJson(json["location"]),
        forecast: Forecast.fromJson(json["forecast"]),
      );

  @override
  List<Object> get props => [location, forecast];
}

enum WindDir { unknown, southWest, west, westSouthWest }

final windDirValues = EnumValues({
  "SW": WindDir.southWest,
  "W": WindDir.west,
  "WSW": WindDir.west,
});

class Forecast extends Equatable {
  final List<ForecastDay> forecastDay;

  const Forecast({
    required this.forecastDay,
  });

  factory Forecast.fromJson(Map<String, dynamic> json) => Forecast(
        forecastDay: List<ForecastDay>.from(
          json["forecastday"].map(
            (x) => ForecastDay.fromJson(x),
          ),
        ),
      );

  @override
  List<Object> get props => [forecastDay];
}

class ForecastDay extends Equatable {
  final DateTime date;
  final int dateEpoch;
  final Day day;
  final List<Hour> hour;

  const ForecastDay({
    required this.date,
    required this.dateEpoch,
    required this.day,
    required this.hour,
  });

  factory ForecastDay.fromJson(Map<String, dynamic> json) => ForecastDay(
        date: DateTime.parse(json["date"]),
        dateEpoch: json["date_epoch"],
        day: Day.fromJson(json["day"]),
        hour: List<Hour>.from(
          json["hour"].map(
            (x) => Hour.fromJson(x),
          ),
        ),
      );

  @override
  List<Object> get props => [date, dateEpoch, day, hour];
}

